package com.wanghuiwen.user.vo;

public class MessageVo {
    private int count;
    private int event;
    private int newset;
    private int notification;
    private int referral;
    private int thank;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getEvent() {
        return event;
    }

    public void setEvent(int event) {
        this.event = event;
    }

    public int getNewset() {
        return newset;
    }

    public void setNewset(int newset) {
        this.newset = newset;
    }

    public int getNotification() {
        return notification;
    }

    public void setNotification(int notification) {
        this.notification = notification;
    }

    public int getReferral() {
        return referral;
    }

    public void setReferral(int referral) {
        this.referral = referral;
    }

    public int getThank() {
        return thank;
    }

    public void setThank(int thank) {
        this.thank = thank;
    }
}
